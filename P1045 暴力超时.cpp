#include <bits/stdc++.h>

using namespace std;
#define ll long long
int a[510];

int main(void)
{
	ll n;
	cin >> n;
	ll x = ceil((double)n * log10(2));
	cout << x << endl;
	a[0] = 2; int amax = 1;
	for (int i = 1; i != n; i++) {
		int jw = 0;
		for (int p = 0; p < amax; p++)
		{
			a[p] = a[p] * 2 + jw;
			jw = a[p] / 10;
			a[p] %= 10;
		}
		if (jw > 0 && amax < 500)
		{
			a[amax] = jw;
			amax++;
		}
	}
	a[0]--; int hs = 0;
	for (int i = 0; i != 500 - amax; i++) {
		cout << 0; hs++;
		if (hs == 50) {
			cout << endl; hs = 0;
		}
	}if (hs == 50) {
		cout << endl; hs = 0;
	}
	for (int i = amax - 1; i >= 0; i--) {
		
		cout << a[i];
		hs++;
		if (hs == 50) {
			cout << endl; hs = 0;
		}
	}
	return 0;
}

